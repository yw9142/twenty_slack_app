import type { RoutePayload } from 'twenty-sdk';

import { SAVE_QUERY_ANSWER_FUNCTION_UNIVERSAL_IDENTIFIER } from 'src/constants/universal-identifiers';
import { defineLogicFunction } from 'src/utils/define-logic-function';
import { handleSaveQueryAnswerRoute } from 'src/utils/codex-tools';

const handler = async (
  event: RoutePayload<Record<string, unknown> | string | null>,
): Promise<Record<string, unknown>> => handleSaveQueryAnswerRoute(event);

export default defineLogicFunction({
  universalIdentifier: SAVE_QUERY_ANSWER_FUNCTION_UNIVERSAL_IDENTIFIER,
  name: 'save-query-answer',
  description: 'Stores a query answer generated by the Codex runner',
  timeoutSeconds: 10,
  handler,
  httpRouteTriggerSettings: {
    path: '/tools/save-query-answer',
    httpMethod: 'POST',
    isAuthRequired: false,
    forwardedRequestHeaders: ['x-tool-shared-secret', 'content-type'],
  },
});
